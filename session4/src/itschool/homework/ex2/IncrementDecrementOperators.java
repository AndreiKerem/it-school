package itschool.homework.ex2;

public class IncrementDecrementOperators {
    public static void main(String[] args) {

        int a = 10;

        System.out.println("Original value of the variable = " + a);
        System.out.println("The Pre-Increment value of the variable = " + ++a);
        System.out.println("The Pre-Decrement value of the variable = " + --a);
        System.out.println("The Post-Increment value of the variable = " + a++);
        System.out.println("The Post-Decrement value of the variable = " + a--);
        System.out.println("The last value of the variable = " + a);
    }
}
